/**
 * Package : comportement
 * 
 * Définition :
 * Contient les design patterns du GOF qui gèrent les interactions et la répartition des responsabilités entre les objets.
 * 
 * Objectif :
 * Faciliter la communication, la flexibilité et l'organisation du comportement des objets dans le système.
 * 
 * Exemples inclus :
 * - Observer : notification automatique des changements d'état.
 * - Strategy : choix dynamique d'algorithme.
 * - Command : encapsulation d'une requête.
 * - Chain of Responsibility : passage de requête le long d'une chaîne.
 * - Interpreter : interprétation d'expressions.
 * - Iterator : parcours d'une collection.
 * - Mediator : centralisation des interactions.
 * - Memento : sauvegarde et restauration d'état.
 * - State : changement dynamique de comportement.
 * - Template Method : structure d'algorithme personnalisable.
 * - Visitor : ajout d'opérations sans modifier les objets.
 */
package designPattern.comportement;
